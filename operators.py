#Demonstrate Arithmetic Operations

x = 8 + 8 
print(x, "= 8 + 8")
x = 8 - 7
print (x, "= 8 - 7")
x = 6 * 5
print(x, "= 6 * 5")
x = 5 / 4
print(x, "= 5 / 4")
x = 5 // 4
print(x, "= 5 // 4")
x = 5 ** 4
print(x, "= 5 ** 4")

#Demonstrate Comparison Operations

x = (5 == 2 + 3)
print('5 == 2 + 3 is', x)
x = (5 != 2 + 3)
print('5 != 2 = 3 is', x)
x = (2 < 3)
print('2 < 3 is', x)
x = (2 > 3)
print('2 > 3 is', x)
x = (4 <= 7)
print('4 <= 7 is', x)
x = (4 >= 7)
print('4 >= 7 is', x)